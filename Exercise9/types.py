# name and lastname are variables
# 'Sadia' and 'Ali' are strings due to quotations
# = is a right associative assignment operator
name = 'Sadia'
lastname = 'Ali'

# print is a built-in function
# separating the string variables with comma (adds the space automatically)
print(name, lastname)

# a list shown by square brackets
fullname = [name, lastname]
print(fullname)

# dictionary stores key value pairs shown by curly brackets
# 'first' and 'last' are keys, the variables name and lastname are values
namelist = {'first': name, 'last': lastname}
print(namelist)



